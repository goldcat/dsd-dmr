float
dmr_filter(float sample)
{
  float sum = 0.0f;
  unsigned int i;

  for (i = 0; i < NZEROS; i++)
    xv[i] = xv[i+1];

  xv[NZEROS] = sample; // unfiltered sample in

  for (i = 0; i <= NZEROS; i++)
    sum += (xcoeffs[i] * xv[i]);

  return (sum * ngain); // filtered sample out
}

